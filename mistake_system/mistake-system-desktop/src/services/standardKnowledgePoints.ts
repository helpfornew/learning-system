// 获取指定科目的标准知识点列表
export const getSubjectKnowledgePoints = (subject: string): string[] => {
  // 处理可能的科目名称变体
  const normalizedSubject = subject.trim();

  if (normalizedSubject.includes('数学')) return STANDARD_KNOWLEDGE_POINTS.数学;
  if (normalizedSubject.includes('物理')) return STANDARD_KNOWLEDGE_POINTS.物理;
  if (normalizedSubject.includes('化学')) return STANDARD_KNOWLEDGE_POINTS.化学;
  if (normalizedSubject.includes('英语')) return STANDARD_KNOWLEDGE_POINTS.英语;
  if (normalizedSubject.includes('语文')) return STANDARD_KNOWLEDGE_POINTS.语文;
  if (normalizedSubject.includes('政治') || normalizedSubject.includes('思想政治')) return STANDARD_KNOWLEDGE_POINTS.政治;
  if (normalizedSubject.includes('生物')) return STANDARD_KNOWLEDGE_POINTS.生物;
  if (normalizedSubject.includes('历史')) return STANDARD_KNOWLEDGE_POINTS.历史;
  if (normalizedSubject.includes('地理')) return STANDARD_KNOWLEDGE_POINTS.地理;

  // 如果科目不在预定义列表中，返回空数组
  return [];
};
